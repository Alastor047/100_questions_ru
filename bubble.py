"""считываем длинну массива"""
n = int(input())

"""считываем строку, делаем список"""
a = [int(i) for i in input().split()]

"""то же самое, но через map"""
a = list(map(int, input().split()))

"""переменная для проверки произошла ли итеррация"""
flag = 1

"""запускаем цикл столько раз - сколько элементов в списке"""
for run in range(n-1):

    """итерируемся по каждому элементу списка при этом
    не перебирая элементы которые уже отсортированы в конец(-run)"""
    for i in range(n-1-run):

        """условие при котором первый проитерированный
        элемент больше следующего"""
        if a[i] > a[i+1]:

            """если условие сверху верно, меняем элементы местами"""
            a[i], a[i+1] = a[i+1], a[i]
            """если заменапроизошла, флаг становится равным 1"""
            flag = 1

    """если флаг == 1"""
    if flag == 1:
        """печатаем результат итеррации"""
        print(' '.join(map(str, a)))
        """такая же печать только с помощью распаковки"""
        print(*a)

        """
        делаем флаг равным нулю, в таком случае если при следующей итеррации
        замен не произойдет то флаг останется равен нулю,
        условие if flag == 1 не выполнится и вывода на печать не будет
        """
        flag = 0
        
            
